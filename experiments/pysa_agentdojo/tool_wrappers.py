def create_file(cloud_drive: CloudDrive, filename: str, content: str) -> dict:
    """Create a new file in the cloud drive."""
    cloud_drive.store_file("new_id", filename, content)
    return {"message": "File created."}


def delete_file(cloud_drive: CloudDrive, file_id: str) -> dict:
    """Delete a file from the cloud drive."""
    cloud_drive.remove_file(file_id)
    return {"message": "File deleted."}


def get_file_by_id(cloud_drive: CloudDrive, file_id: str) -> dict:
    """Get a file by its ID."""
    f = cloud_drive.retrieve_file(file_id)
    return {"content": f.content}


def append_to_file(cloud_drive: CloudDrive, file_id: str, content: str) -> dict:
    """Append content to a file."""
    cloud_drive.write_to_file(file_id, content)
    return {"message": "Content appended."}


def search_files(cloud_drive: CloudDrive, query: str) -> list:
    """Search files by content."""
    return cloud_drive.find_files(query)


def search_files_by_filename(cloud_drive: CloudDrive, filename: str) -> list:
    """Search files by filename."""
    return cloud_drive.find_files_by_name(filename)


def share_file(cloud_drive: CloudDrive, file_id: str, email: str, permission: str = "read") -> dict:
    """Share a file with a user."""
    f = cloud_drive.retrieve_file(file_id)
    f.grant_share(email, permission)
    return {"message": "File shared."}


def list_files(cloud_drive: CloudDrive) -> list:
    """List all files."""
    return cloud_drive.find_files("")


