def create_calendar_event(calendar: Calendar, title: str, start_time: str, end_time: str,
                          description: str = "", participants: list = [], location: str = "") -> dict:
    """Create a new calendar event. Also sends invites to participants."""
    event = CalendarEvent(title=title, start_time=start_time, end_time=end_time,
                          description=description, participants=participants, location=location)
    calendar.add_event("new_id", event)
    # Side effect: sends invitation emails to participants (EGRESS)
    for p in participants:
        msg = Message(sender="calendar", recipient=p, body=f"Invite: {title}")
    return {"message": "Event created."}


def cancel_calendar_event(calendar: Calendar, event_id: str) -> dict:
    """Cancel a calendar event. Also notifies participants."""
    calendar.remove_event(event_id)
    return {"message": "Event cancelled."}


def reschedule_calendar_event(calendar: Calendar, event_id: str,
                              new_start_time: str, new_end_time: str) -> dict:
    """Reschedule a calendar event."""
    calendar.update_event(event_id, start_time=new_start_time, end_time=new_end_time)
    return {"message": "Event rescheduled."}


def add_calendar_event_participants(calendar: Calendar, event_id: str, participants: list) -> dict:
    """Add participants to a calendar event."""
    calendar.update_event(event_id, participants=participants)
    for p in participants:
        msg = Message(sender="calendar", recipient=p, body="You were added to an event")
    return {"message": "Participants added."}


def get_day_calendar_events(calendar: Calendar, day: str) -> list:
    """Get calendar events for a specific day."""
    return calendar.get_events_for_day(day)


def search_calendar_events(calendar: Calendar, query: str, date: str = "") -> list:
    """Search calendar events by query."""
    return calendar.search(query, date)


def get_current_day() -> str:
    """Get the current day."""
    return "2024-01-01"


