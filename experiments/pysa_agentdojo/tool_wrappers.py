def send_money(account: BankAccount, recipient: str, amount: float, subject: str, date: str) -> dict:
    """Sends a transaction to the recipient."""
    transaction = Transaction(id=1, sender=account.iban, recipient=recipient,
                              amount=amount, subject=subject, date=date, recurring=False)
    account.record_transaction(transaction)
    return {"message": f"Transaction to {recipient} for {amount} sent."}


def schedule_transaction(account: BankAccount, recipient: str, amount: float,
                         subject: str, date: str, recurring: bool) -> dict:
    """Schedule a transaction."""
    transaction = Transaction(id=1, sender=account.iban, recipient=recipient,
                              amount=amount, subject=subject, date=date, recurring=recurring)
    account.record_scheduled_transaction(transaction)
    return {"message": "Transaction scheduled."}


def update_scheduled_transaction(account: BankAccount, id: int, recipient: str = "",
                                 amount: float = 0, subject: str = "", date: str = "",
                                 recurring: bool = False) -> dict:
    """Update a scheduled transaction."""
    for t in account.scheduled_transactions:
        if t.id == id:
            account.update_scheduled_transaction_fields(
                t, recipient=recipient, amount=amount, subject=subject, date=date)
            return {"message": "Transaction updated."}
    return {"error": "Not found"}


def get_balance(account: BankAccount) -> float:
    """Get the balance of the account."""
    return account.balance


def get_iban(account: BankAccount) -> str:
    """Get the IBAN of the current bank account."""
    return account.iban


def get_most_recent_transactions(account: BankAccount, n: int) -> list:
    """Get the most recent transactions."""
    return account.get_recent_transactions(n)


def get_scheduled_transactions(account: BankAccount) -> list:
    """Get the scheduled transactions."""
    return account.get_all_scheduled()


